package com.sagyo.join;

public class JoinDTO
{
	private String badgepointName, nickname, userId, contents, joinDate;

	public String getBadgepointName()
	{
		return badgepointName;
	}

	public void setBadgepointName(String badgepointName)
	{
		this.badgepointName = badgepointName;
	}

	public String getNickname()
	{
		return nickname;
	}

	public void setNickname(String nickname)
	{
		this.nickname = nickname;
	}

	public String getUserId()
	{
		return userId;
	}

	public void setUserId(String userId)
	{
		this.userId = userId;
	}

	public String getContents()
	{
		return contents;
	}

	public void setContents(String contents)
	{
		this.contents = contents;
	}

	public String getJoinDate()
	{
		return joinDate;
	}

	public void setJoinDate(String joinDate)
	{
		this.joinDate = joinDate;
	}

	
}
